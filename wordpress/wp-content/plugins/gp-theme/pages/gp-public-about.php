	        <li>October 2007 - Held Australia's first eco fashion parade gaining significant national media attention.</li>
	        <li>February 2008 - Produced first quarterly magazine, launching into 5 publications per year and growing to around 20 full time staff and being largely recognised as one of Australia's most prominent green brands.</li>
    	    <li>June 2008 - Launched first green news portal style and directory website, growing to over 2,000 paying clients and 15,000 members.</li>
	        <li>March 2010 - After a strong three years of print publishing, Green Pages produced the final physical publication and moved from a media business to a 100% technology business.</li>

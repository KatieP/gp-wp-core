        <h3><a>Where and how did the company start?</a></h3>
        <p>Back in 2005 was a time before there were funky green magazines, eco blogs, triple bottom line corporate conferences,
        'sustainability managers' and progressive design agencies. The concept of 'green' was still synonymous with falafels and 
        messy hair.</p>
        <p>Newspapers very rarely wrote articles on environmental issues, often perceived as part of fringe culture. 
        Yet there was a powerful latent desire from business owners, professionals and consumers for greener business practices 
        that was starting to bubble up to the surface of corporate Australia.</p>
        <p>Green Pages founder, Katie Patrick, was a young environmental engineer working in the green property industry. She 
        saw a strong need to pull together all of the disparate environmental product information and also 're-brand' the green 
        movement as fun, sexy, cool and progressive. She had compiled a significant database of green suppliers and had started 
        to make progress towards developing a commercial green directory called Green Pages. Her main focus at that time was to 
        help architects to find sustainable materials and businesses to find eco-friendly office supplies.</p>
        <p>Katie worked out of her share-house bedroom in Sydney's inner suburb of Redfern and with little more that a bicycle, 
        a brochure and some business cards, eventually launched Australia's first print and online directory listing over 3,000 
        sustainable products and services. Starting with an annual directory, Green Pages grew to be a quarterly magazine, a 
        business directory plus an online portal of sustainable news and products.</p> 
        <p>The Directory itself grew to include over 9000 listings and the company grew to a successful 20 staff and $1.5 million 
        dollar turnover. In 2010 Green Pages made a bold move to step away from its successful print publishing business to focus 
        on a more progressive technology product.</p>
        <p>Read about this in an article written by Katie called 'Goodbye Print. Hello World'. In 2013 Katie and Jesse Browne 
        created a new San Francisco based internet startup from which they released the new global community platform for environmental
        change at greenpag.es.</p>
        
